<template>
  <b-container>
    <h1 class="title">Add new Ads</h1>

    <b-form @submit="onSubmit" @reset="onReset" v-if="show">
        <b-form-group
        id="adsCategorieGroup"
        label="Ads Categorie:"
        label-for="adsTitle"
        description="Please select the ADS categorie"
      >
        <b-form-select
          id="Categorie"
          type="select"
          v-model="form.categorie"         
          required
          placeholder="Please select a categorie"
        >
        <!-- This slot appears above the options from 'options' prop -->
      <template slot="first">
        <option :value="null" disabled>-- Please select an categorie --</option>
      </template>
        <option v-for="categorie in categories" :value="categorie.id">{{ categorie.label }}</option>
        </b-form-select>
      </b-form-group>

        <b-form-group
        id="adsPhoneGroup"
        label="Ads Phone:"
        label-for="adsPhone"
        description="Please enter your phone number"
      >


        <vue-tel-input required  @onInput="onInput" :preferredCountries="['us', 'gb', 'ua','fr']" >
        </vue-tel-input>
         
 </b-form-group>


      <b-form-group
        id="adsTitleGroup"
        label="Ads Title:"
        label-for="adsTitle"
        description="Please enter the ADS title"
      >
        <b-form-input
          id="adsTitle"
          type="text"
          v-model="form.title"
          required
          placeholder="Enter Ads Title"
        />
      </b-form-group>

      <b-form-group
        id="adsDescriptionGroup"
        label="Ads Description:"
        label-for="adsDescription"
        description="Please enter the ADS description"
      >
        <b-form-textarea
          id="adsDescription"
          v-model="form.description"
          placeholder="Enter something..."
          rows="3"
          max-rows="6"
          required
        />

        <pre class="mt-3 mb-0">{{ form.description }}</pre>
      </b-form-group>
      <b-form-group
        id="adsPicturesGroup"
        label="Ads Pictures:"
        label-for="adsPictures"
        description="Please upload ADS pictures"
      >
        <b-form-file
          id="adsPictures"
         
          v-model="form.pictures"
          :state="Boolean(form.pictures)"
          placeholder="Choose pictures picture..."
          drop-placeholder="Drop file here..."
          :multiple="Boolean(true)"
          accept="image/*"
        />
      </b-form-group>

      <button class="btn btn-primary">Add new ADS</button>
    </b-form>
  </b-container>
</template>
<script>
import {APIAdsService} from '../services/APIAdsService'
const apiService = new APIAdsService();
export default {
  data() {
    return {       
      form: {
        title: "",
        description: "",
        pictures: null,
        categorie:"",
        phone:{
            number: '',
            isValid: false,
            country: undefined,
        }
      },
      categories : [],
      show: true
    };
  } ,
  created() {   
    
    this.$store.subscribe((mutation, state) => {
           if(mutation.type=='registerObitDbInstance'){
                 this.getCategories();                   
           }
    });

  },
  mounted() {
     this.getCategories(); 
  },
  methods: {
    
    onInput({ number, isValid, country }) {
      this.form.phone.number = number;
      this.form.phone.isValid = isValid;
      this.form.phone.country = country ;
    },  
    getCategories() {
        if(typeof this.$store.state.orbitDbInstance !=undefined )
        this.categories = this.$store.state.orbitDbInstance.categories;
    },
    onSubmit(evt) {
      evt.preventDefault();
       
        //Form is valid , we add the new ads to the orbitDB database
        //Call the express server with orbit DB
        let formData = new FormData();
       
        formData.append('title', this.form.title);
        formData.append('description', this.form.description);
        formData.append('categorie', this.form.categorie);
        formData.append('phone', JSON.stringify(this.form.phone));

        for( var i = 0; i < this.form.pictures.length; i++ ){
            let file = this.form.pictures[i];
            formData.append('files[' + i + ']', file);
        }

       apiService.createAds(formData).then((result)=>{

         
        //Get New ADS added to orbitDB
        this.$store.state.contractInstance().payNewAds(result.data.hashBytes32,{
            value: this.$store.state.web3.web3Instance().toWei('0.1', 'ether'),
            from: this.$store.state.web3.coinbase
        },(err, result) => {
            if (err) {
            console.log(err);
            //this.pending = false;
            } else {
                console.log(result);
            }
        });

         
        },(error)=>{
            console.log(error);
        });   


      
    },
    onReset(evt) {
      evt.preventDefault();
      /* Reset our form values */
      this.form.title = "";
      this.form.description = "";
      this.form.categorie = "";
      this.form.pictures = null;
      this.form.phone = {
            number: '',
            isValid: false,
            country: undefined,
        };
      /* Trick to reset/clear native browser form validation state */
      this.show = false;
      this.$nextTick(() => {
        this.show = true;
      });
    }
  }
};
</script>